"""Faça um Programa que peça as 4 notas bimestrais e mostre a média."""

nota1 = int(input('Insira a nota bimestral 1: '))
nota2 = int(input('Insira a nota bimestral 2: '))
nota3 = int(input('Insira a nota bimestral 3: '))
nota4 = int(input('Insira a nota bimestral 4: '))

media = (nota1 + nota2 + nota3 + nota4) / 4 

print(f"A média das notas bimestrais é {media}")